function keyPressed() {
  if(keyCode==37) {
    initShelock()
    progressPrev += progress
    for(let i=0;i<keyCodes.length;i++) {
        envo[i].triggerRelease(osc[i])
        envo_a[i].triggerRelease(osc_a[i])
        envo_b[i].triggerRelease(osc_b[i])
    }
    drawRings(80)
  }
  if(keyCode==39) {
    initGOT()
    progressPrev += progress
    for(let i=0;i<keyCodes.length;i++) {
        envo[i].triggerRelease(osc[i])
        envo_a[i].triggerRelease(osc_a[i])
        envo_b[i].triggerRelease(osc_b[i])
    }
    drawRings(60)
  }
  if(keyCode==38) {
    for(let i=0;i<keyCodes.length;i++) {
        envo[i].triggerRelease(osc[i])
        envo_a[i].triggerRelease(osc_a[i])
        envo_b[i].triggerRelease(osc_b[i])
    }
    listenOnly = true
    drawRings(20)
  }
  if(keyCode==40) {
    for(let i=0;i<keyCodes.length;i++) {
        envo[i].triggerRelease(osc[i])
        envo_a[i].triggerRelease(osc_a[i])
        envo_b[i].triggerRelease(osc_b[i])
    }
    listenOnly = false
    drawRings(40)
  }
  if(keyCode==8) {
    progressPrev += progress
    for(let i=0;i<keyCodes.length;i++) {
        envo[i].triggerRelease(osc[i])
        envo_a[i].triggerRelease(osc_a[i])
        envo_b[i].triggerRelease(osc_b[i])
    }
    drawRings(0)
  }
  for(let i=0;i<keyCodes.length;i++) {
    if (keyCode === keyCodes[i]) {
      osc[i].freq(midiToFreq(root_b+noteKeyMap[i+keyBias]))
      
      envo[i].triggerAttack(osc[i])
      if (patternsInDrawing[i]==null){
        patternsInDrawing[i] = {
          x: random(width),
          y: random(0.8*height),
          r: 0,
          h: 10+20*(i%4),
          b: 80,
          a: random(PI/3),
        }
      }
      ripplesInDrawing.unshift({
        x: 60+30*(i),
        y: height-50,
        r: 0,
        d: 20,
        b: 80,
        h: 10+20*((i)%4),
      })
    }
  }
}

function keyReleased() {
  for(let i=0;i<keyCodes.length;i++) {
    if (keyCode === keyCodes[i]) {
      envo[i].triggerRelease(osc[i])
      patternsFading.push(patternsInDrawing[i])
      patternsInDrawing[i] = null
    }
  }
}

function drawPatterns() {
  let deleteArray = []
  for(let i=0;i<patternsFading.length;i++){
    p = patternsFading[i]
    if(p!=null) {
      p.r++
      p.b--
      p.a+=0.05
      stroke(p.h,80,p.b)
      strokeWeight(p.r/10)
      drawHexagon(p)
      if(p.b<0) deleteArray.push(i)
    }
  }
  for(let i=deleteArray.length-1;i>=0;i--){
    patternsFading.splice(i,1)
  }
  patternsInDrawing.forEach(function(p) {
    if(p!=null) {
      p.r+=2
      p.a+=0.05
      stroke(p.h,80,p.b)
      strokeWeight(p.r/10)
      // fill(p.h,80,p.b)
      drawHexagon(p)
    }
  })  
}

function drawHexagon(p) {
  push()
  translate(p.x, p.y)
  rotate(p.a)
  for(let i=0;i<6;i++) {
    rotate(PI/3)
    beginShape()
    vertex(0,0)
    vertex(p.r/2, p.r*sqrt(3)/2)
    vertex(-p.r/2, p.r*sqrt(3)/2)
    endShape()
  }
  ellipse(0, 0, p.r/3, p.r/3)
  pop()
}

function drawRipples() {
  let deleteArray = []
  let r
  for(let i=ripplesInDrawing.length-1;i>=0;i--) {
    r = ripplesInDrawing[i]
    stroke(r.h, 80, r.b--)
    strokeWeight(r.d)
    ellipse(r.x, r.y, r.r, r.r++)
    if(r.b<0) deleteArray.push(i)
  }
  for(let i=0; i<deleteArray.length;i++) {
    ripplesInDrawing.splice(deleteArray[i], 1)
  }
}

function drawRings(h) {
  for(let i=6;i>0;i--) {
    ripplesInDrawing.unshift({
      x: width/2,
      y: height/2,
      r: 100*i,
      d: 50,
      b: 80,
      h: h,
    })
  }
}

