    fill(0, 0, 100)
    rect(-width,height-810,2*width, 285)
    fill(0, 0, 0)
    text("Press BackSpace to reset\nPress Left and Right to switch music\nPress Up and Down to enable/disable Listen-Only mode", 540, height-770)
    fill(0)
